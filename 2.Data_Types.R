# Data types in R-Programming

# 1. Numeric 
numeric_num <- 23
numeric_num2  <- 28.9
class(numeric_num)
class(numeric_num2)

#it prints  "numeric"
# "class()" is used to print datatype of variables






# 2. Integer 
int_num <- 89L
class(int_num)

# it prints "integer"





# 3. complex
com_num <- 2+7i
class(com_num)

# it prints "complex"






# 4. character
char_data <- "kiran c r"
char_data2 <- 'k'
class(char_data)
class(char_data2)

# both are prints "character"



# 5.logical
log_data <- TRUE
log_data1 <- FALSE
log_data2 <- T
log_data3 <- F

class(log_data)
class(log_data1)
class(log_data2)
class(log_data3)

#all are printed "logical"




# another method to check the data type of an variable
typeof(log_data)




